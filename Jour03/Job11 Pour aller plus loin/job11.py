#La fonction input() est une fonction intégrée de Python qui permet de demander à l'utilisateur de saisir des données depuis le clavier.
la_chaîne_de_caractères = input("Ecrire des caractères ")

if 'e' in la_chaîne_de_caractères:
    print("'e' dans la chaîne de caractères.")
else:
    print( "il n'y a pas le caractère 'e' dans la chaîne de caractères.")

